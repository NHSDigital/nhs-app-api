export const isNhsAppRouteName = name => !(!names.find(n => n === name));

export const findByRedirectEnum = (name) => {
  switch (name) {
    case AppPage.HOME_PAGE:
      return INDEX_NAME;
    case AppPage.APPOINTMENTS:
      return APPOINTMENTS_NAME;
    case AppPage.PRESCRIPTIONS:
      return PRESCRIPTIONS_NAME;
    case AppPage.HEALTH_RECORDS:
      return HEALTH_RECORDS_NAME;
    case AppPage.ADVICE:
      return ADVICE_NAME;
    case AppPage.MESSAGES:
      return MESSAGES_NAME;
    default:
      return undefined;
  }
};

